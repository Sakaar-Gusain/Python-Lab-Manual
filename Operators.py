# Arithmetic Operators
x = 10
y = 3
print("Arithmetic Operators:")
print("x + y =", x + y)  # Addition
print("x - y =", x - y)  # Subtraction
print("x * y =", x * y)  # Multiplication
print("x / y =", x / y)  # Division
print("x // y =", x // y)  # Integer Division
print("x % y =", x % y)  # Modulus
print("x ** y =", x ** y)  # Exponentiation
print()

# Comparison Operators
a = 5
b = 10
print("Comparison Operators:")
print("a == b is", a == b)  
print("a != b is", a != b)  
print("a > b is", a > b)    
print("a < b is", a < b)    
print("a >= b is", a >= b)  
print("a <= b is", a <= b)  
print()

# Logical Operators
p = True
q = False
print("Logical Operators:")
print("p and q is", p and q)  # Logical AND
print("p or q is", p or q)    # Logical OR
print("not p is", not p)      # Logical NOT
print()

# Assignment Operators
x = 5
print("Assignment Operators:")
x += 3  
print("x += 3 -> x =", x)
x -= 2  
print("x -= 2 -> x =", x)
x *= 4  
print("x *= 4 -> x =", x)
x /= 2  
print("x /= 2 -> x =", x)
x %= 3  
print("x %= 3 -> x =", x)
x **= 2  
print("x **= 2 -> x =", x)
print()

# Membership Operators
list1 = [1, 2, 3, 4, 5]
print("Membership Operators:")
print("2 in list1 is", 2 in list1)      # in
print("6 not in list1 is", 6 not in list1)  # not in
print()

# Identity Operators
x = 10
y = 10
z = 5
print("Identity Operators:")
print("x is y is", x is y)  # is
print("x is z is", x is z)  # is
print("x is not z is", x is not z)  # is not
